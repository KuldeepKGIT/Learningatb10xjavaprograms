package Task_5th_feb;

public class printnum_1_10 {
    public static void main(String[] args) {
        System.out.println("print the counting up to 10 using for loop");
        int i;
        for ( i=1; i<=20; i++)
        {
            if (i%2==0){
                System.out.println(i);
            }
        }
    }
}
